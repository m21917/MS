		<p>
			&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp本次「明日創作主題文章」設計競賽，參賽作品內容主軸須具「知識性」，教師可配合課程單元、活動、節慶、生活經驗等發想主題文章，藉此引導學生進行主題寫作。每一主題作品須包含：
		</p>
		<p>1.	主題名稱：限20字以內。</p>
		<p>2.	主題領域：對應九年一貫課程相關領域：「語文」、「社會與人文」、「自然與科學」、「健康與運動」，及相關子領域。</p>
		<p>3.	適用對象：分為國小低、中、高年級及國中。</p>
		<p>4.	主題文章：編輯4篇具異質性、不同角度之範文供學生閱讀，讓學生能了解主題的內容範疇，進而寫作。範文可自創或使用他人創作(請參考注意事項)，字數建議國小一篇約300-600字，國中一篇約800-1200字。</p>
		<p>5.	啟發問題：根據主題發想相關、具層次性的問題，可引導學生進行寫作架構的思考。</p>
		<p>6.	關鍵詞：至少設置3-5個關鍵詞，以供檢索。</p>

