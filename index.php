	<!-- 活動宗旨 -->
	<div id="section1">
		<h3 class="contentTitle"><strong>活動宗旨:</strong></h3>
		<p>
		&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp明日創作藉由主題文章之引導，讓學生多元發想、自由寫作，並以啟發問題使學生寫作之內容架構更完整；也希望學生從明日創作的多讀多寫，能激發寫作想法、學習探索主題知識，並延伸學習興趣，進而培養終身學習習慣。
		</p>
		<p>
		&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp明日創作實作包含四個步驟，分別為「讀、創、聊、登」。「讀」指的是讓學生透過異質性閱讀文章的方式，對於寫作主題有不同面向的刺激。「創」指的是學生閱讀文章後，經由啟發問題的引導，先構思關於寫作主題想要寫的內容，接著再組織這些寫作想法成為完整文章初稿。「聊」指的是學生相互閱讀同儕的初稿，並反饋建議修改意見。寫作者收到這些意見之後，可據以修改草稿。「登」指的是學生們根據同儕意見修改初稿後，將完整文章發表與全班學生分享，提供學生一個作品賞閱的舞台。
		</p>
		<p>
		&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp因此，主題文章的設計，可說是明日創作的核心之一。透過設計這些主題文章，學生們從這些文章汲取關於主題的相關知識與想法，再透過啟發問題聚斂寫作內容。透過明日創作的四個步驟，讓學生不再苦於無寫作想法，不知如何下筆的困擾。
		</p>
		<p>
		&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp為使桃園市師生有豐富、多元的主題文章，可供教學與學習，同時促進教師專業發展，爰於104年度規劃辦理兩期徵稿競賽，鼓勵教師、社會人士投入明日創作主題文章製作的行列，激發自創教學教材的創意與熱忱，共享教學資源。
		</p>
		<p><strong>主辦單位：</strong>桃園市政府教育局</p>
